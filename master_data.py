def _exists(doctype: str, field: str, value: str) -> str | None:
    return frappe.db.get_value(doctype, {field: value}, "name")


def _insert(doctype: str, values: dict):
    doc = frappe.get_doc({"doctype": doctype, **values})
    doc.flags.ignore_permissions = True
    doc.flags.ignore_version = True
    doc.insert(ignore_permissions=True)
    return doc


def _catalog_path(filename: str):
    from pathlib import Path
    return Path(__file__).resolve().parent / "reference_data" / filename


def _load_catalog_rows(filename: str, required_columns: tuple[str, ...] = ()):
    import csv
    path = _catalog_path(filename)
    if not path.exists():
        frappe.throw(f"Missing WAFD ONE reference catalog: {filename}")
    with path.open(encoding="utf-8-sig", newline="") as source:
        reader = csv.DictReader(source)
        columns = tuple(reader.fieldnames or ())
        missing = [column for column in required_columns if column not in columns]
        if missing:
            frappe.throw(f"Invalid reference catalog {filename}; missing columns: {', '.join(missing)}")
        return list(reader)


def _install_hotel_catalog(counts):
    for row in _load_catalog_rows("madinah_hotels_master_v6_1_0.csv", ("hotel_name", "city", "district", "address")):
        name = (row.get("hotel_name") or "").strip()
        if not name:
            continue
        existing = _exists("WAFD Hotel", "hotel_name", name)
        values = {
            "hotel_name": name,
            "city": row.get("city") or "المدينة المنورة",
            "district": row.get("district") or "المدينة المنورة",
            "address": row.get("address") or "المدينة المنورة",
            "status": ACTIVE,
        }
        if not existing:
            _insert("WAFD Hotel", values)
            counts["hotels"] += 1
        else:
            doc = frappe.get_doc("WAFD Hotel", existing)
            changed = False
            for field in ("city", "district", "address"):
                if values.get(field) and not doc.get(field):
                    doc.set(field, values[field])
                    changed = True
            if changed:
                doc.flags.ignore_permissions = True
                doc.flags.ignore_version = True
                doc.save(ignore_permissions=True)


def _install_ingredient_catalog(counts):
    from frappe.utils import flt
    for row in _load_catalog_rows("ingredients_master_v6_1_0.csv", ("ingredient_name", "item_code", "category", "uom")):
        name = (row.get("ingredient_name") or "").strip()
        if not name or _exists("WAFD Ingredient", "ingredient_name", name):
            continue
        _insert("WAFD Ingredient", {
            "ingredient_name": name,
            "item_code": row.get("item_code"),
            "category": row.get("category"),
            "uom": row.get("uom"),
            "standard_cost": flt(row.get("standard_cost")),
            "minimum_stock": flt(row.get("minimum_stock")),
            "default_supplier": row.get("default_supplier"),
            "status": ACTIVE,
        })
        counts["ingredients"] += 1


def _install_recipe_catalog(counts):
    import json
    for row in _load_catalog_rows("recipes_master_v6_1_0.csv", ("recipe_name", "meal_category", "yield_quantity", "items_json")):
        name = (row.get("recipe_name") or "").strip()
        if not name or _exists("WAFD Recipe", "recipe_name", name):
            continue
        recipe = frappe.get_doc({
            "doctype": "WAFD Recipe",
            "recipe_name": name,
            "meal_category": row.get("meal_category"),
            "yield_quantity": int(float(row.get("yield_quantity") or 100)),
            "status": RECIPE_ACTIVE,
            "instructions": "وصفة معيارية مرجعية. يجب مراجعة الكميات والأسعار واعتمادها تشغيليًا قبل الإنتاج.",
            "items": [],
        })
        try:
            items = json.loads(row.get("items_json") or "[]")
        except (TypeError, ValueError) as exc:
            frappe.throw(f"Invalid recipe JSON for {name}: {exc}")
        if not isinstance(items, list) or not items:
            frappe.throw(f"Recipe {name} has no valid ingredient rows")
        for item in items:
            if not isinstance(item, list) or len(item) != 2:
                frappe.throw(f"Invalid ingredient row in recipe {name}: {item!r}")
            ingredient, quantity = item
            if frappe.db.exists("WAFD Ingredient", ingredient):
                recipe.append("items", {"ingredient": ingredient, "quantity": quantity})
        if not recipe.items:
            continue
        recipe.flags.ignore_permissions = True
        recipe.flags.ignore_version = True
        recipe.insert(ignore_permissions=True)
        counts["recipes"] += 1


def load_reference_master_data() -> dict[str, int]:
    """Create missing WAFD reference data without touching existing records."""
    counts = {"missions": 0, "hotels": 0, "suppliers": 0, "ingredients": 0, "warehouses": 0, "recipes": 0, "stock_balances": 0}

    for name, country in MISSIONS:
        if not _exists("WAFD Mission", "mission_name", name):
            _insert("WAFD Mission", {"mission_name": name, "country": country, "address": "مكتب شؤون الحجاج - المدينة المنورة", "status": ACTIVE})
            counts["missions"] += 1

    _install_hotel_catalog(counts)

    for name, district, address, lat, lon in HOTELS:
        existing = _exists("WAFD Hotel", "hotel_name", name)
        values = {"hotel_name": name, "city": "المدينة المنورة", "district": district, "address": address, "status": ACTIVE}
        if lat is not None:
            values["latitude"] = lat
        if lon is not None:
            values["longitude"] = lon
        if not existing:
            _insert("WAFD Hotel", values)
            counts["hotels"] += 1
        else:
            # Fill only missing reference fields; never overwrite user-entered values.
            doc = frappe.get_doc("WAFD Hotel", existing)
            changed = False
            for field in ("city", "district", "address", "latitude", "longitude"):
                value = values.get(field)
                if value not in (None, "") and not doc.get(field):
                    doc.set(field, value)
                    changed = True
            if changed:
                doc.flags.ignore_permissions = True
                doc.flags.ignore_version = True
                doc.save(ignore_permissions=True)

    for name, category in SUPPLIERS:
        if not _exists("WAFD Supplier", "supplier_name", name):
            _insert("WAFD Supplier", {"supplier_name": name, "category": category, "address": "المدينة المنورة", "payment_terms": "حسب العقد وأمر الشراء", "status": ACTIVE})
            counts["suppliers"] += 1

    _install_ingredient_catalog(counts)

    for name, code, category, uom, cost, minimum, supplier in INGREDIENTS:
        if not _exists("WAFD Ingredient", "ingredient_name", name):
            _insert("WAFD Ingredient", {"ingredient_name": name, "item_code": code, "category": category, "uom": uom, "standard_cost": cost, "minimum_stock": minimum, "default_supplier": supplier, "status": ACTIVE})
            counts["ingredients"] += 1

    for name, warehouse_type, location in WAREHOUSES:
        if not _exists("WAFD Warehouse", "warehouse_name", name):
            _insert("WAFD Warehouse", {"warehouse_name": name, "warehouse_type": warehouse_type, "location": location, "status": ACTIVE})
            counts["warehouses"] += 1

    _install_recipe_catalog(counts)

    for recipe_name, category, items in RECIPES:
        if _exists("WAFD Recipe", "recipe_name", recipe_name):
            continue
        recipe = frappe.get_doc({"doctype": "WAFD Recipe", "recipe_name": recipe_name, "meal_category": category, "yield_quantity": 100, "status": RECIPE_ACTIVE, "instructions": "وصفة معيارية لعدد 100 حصة. تُراجع الكميات حسب العقد ونوع العبوة قبل بدء الإنتاج.", "items": []})
        for ingredient, quantity in items:
            if frappe.db.exists("WAFD Ingredient", ingredient):
                recipe.append("items", {"ingredient": ingredient, "quantity": quantity})
        recipe.flags.ignore_permissions = True
        recipe.flags.ignore_version = True
        recipe.insert(ignore_permissions=True)
        counts["recipes"] += 1

    # Inventory quantities are operational facts and must never be fabricated.
    # Warehouses and ingredient masters are created, but opening balances are
    # entered only after a physical count or an approved stock movement.

    return counts
